<?php

namespace Database\Factories;

use App\Models\User;
use Illuminate\Database\Eloquent\Factories\Factory;
use Illuminate\Support\Facades\Hash;
use Illuminate\Support\Str;
use Faker\Generator as Faker;

class UserFactory extends Factory
{
    /**
     * The name of the factory's corresponding model.
     *
     * @var string
     */
    protected $model = User::class;

    /**
     * Define the model's default state.
     *
     * @return array
     */
    public function definition()
    {
        $faker = $this->faker;

        return [
            'profile_picture' => config('app.avatar_generator_url') . 'nabil', // Adjust as per your application logic
            'fullname' => $faker->name,
            'email' => $faker->unique()->safeEmail,
            'password' => static::$password ??= Hash::make('password'),
            'nim' => $faker->phoneNumber,
            'user_type' => $faker->name,
            'edu_program' => $faker->name,
            'status' => $faker->name,
            // Uncomment the lines below if you want to include them
            // 'email_verified_at' => now(),
            // 'password' => '$2y$10$92IXUNpkjO0rOQ5byMi.Ye4oKoEa3Ro9llC/.og/at2.uheWG/igi',
            // 'remember_token' => Str::random(10),
        ];
    }

    /**
     * Indicate that the model's email address should be unverified.
     *
     * @return \Database\Factories\UserFactory
     */
    public function unverified()
    {
        return $this->state(function (array $attributes) {
            return [
                'email_verified_at' => null,
            ];
        });
    }
}
